	$(function(){
			var img_id = 2;
			$('#price_2').show();
			
			//$('div#hardcover_menu table tr:odd').css({'background':'#DDD'})
			
			$("li a").animate({'opacity' : 1}).hover(function() {
				$(this).animate({'opacity' : .3});
			}, function() {
				$(this).animate({'opacity' : 1});
			});	
			
			$("button").animate({'opacity' : 1}).hover(function() {
				$(this).animate({'opacity' : .8});
			}, function() {
				$(this).animate({'opacity' : 1});
			});
			
			$("#ul_booktype li img").animate({'opacity' : .5}).hover(function() {
				$(this).animate({'opacity' : 1});
			}, function() {
				if ($(this).hasClass('selected')){				
					$(this).animate({'opacity' :.8});
					$('#ul_booktype li img#'+img_id).addClass('selected').animate({opacity: 1});	
				}else{
					$(this).animate({'opacity' :.5});
				}
			});						
			
			$('#ul_booktype li img#2').css({'background':'#FF912F'}).animate({opacity: 1},'fast');
			
			$("#ul_booktype li img").live('click',function(){
				$('#ul_booktype li img').css({'background':'none'}).animate({opacity: .5}, 'fast');
				img_id= $(this).attr('id');
				$('div.hideDiv').each(function(){
					var this_id = $(this).attr('id');
					$('#'+this_id).hide();
				});	
				$('#price_'+img_id).fadeIn();						
				$('#ul_booktype li img#'+img_id).css({'background':'#FF912F'}).animate({opacity: 1}, 'fast');					
				$('#ul_booktype li img').removeClass('selected');
				$('#ul_booktype li img#'+img_id).addClass('selected');
			});
			
			$("#button_home_next").live('click',function(){			
				//loadFriendsImage();	
				//getCookie();
				//var book_id = $('img.selected').attr('id');				
				$.ajax({
					url 	: '/main/start_create_book',
					type	: 'post',
					data	: {'booksize':img_id},
					success : function(res){
								$("#my_home").empty().html('<div id="friend_img_wrapper"><input type="text" id="my_cover_title" size="48"><br/><div id="tool_alert"></div><ul id="ul_friend_img"></ul><button id="cover_next">Next</button></div>');					
								var _obj = $.parseJSON(res);	
								$('#my_cover_title').val(_obj.book_name);
								var split_obj = _obj.friends_fbid.split(';');
								$.each(split_obj,function(i){
									if (i == 24){
										return false;
									}
									var imgUrl = 'https://graph.facebook.com/'+split_obj[i]+'/picture';
									var x_img = "https://dev.hardcover.me/images/x.png";
									$('#ul_friend_img').append('<li id="'+split_obj[i]+'" class="float_left"><img src="'+imgUrl+'"><a href="#" class="tooltip" title="Remove photo" id="delete"><img id='+split_obj[i]+' class="ximg" src="'+x_img+'"></a></li>').fadeIn('slow');
								});	
								gPicture();							
								tooltip();
					}	
				});
			});
			
			
	})
</script>