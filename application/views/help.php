A Few Easy Steps<br>
1. First you have to give hardcover permission to retrieve your Facebook albums<br>
2. Create book - Select if it is &quot;Album for me&quot;, &quot;Album for friends&quot;, 
&quot;Group Gift&quot;.<br>
3. Select the book type/size.<br>
4. Design your book cover , enter book title and book cover<br>
5. Filter the facebook data you want to put in your book. You can select &quot;Status 
Updates&quot;, &quot;Posts I Like&quot;, &quot;Photos from Album&quot; and &quot;Photo 
Size&quot;<br>
6. Add book description that will show in the preface<br>
7. Preview and Edit<br>
8. Share and Print<br>
